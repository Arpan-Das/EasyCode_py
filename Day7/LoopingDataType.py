"""
Looping through Data Types in Python
"""

# Looping through a list
list1 = [10, 20, 30, 40, 50]
print("Looping through list1:")
for item in list1:
    print(item)

# Looping through a tuple
tuple1 = (100, 200, 300, 400, 500)
print("Looping through tuple1:")    
for item in tuple1:
    print(item)

# Looping through a string
string1 = "Hello"
print("Looping through string1:")
for char in string1:
    print(char)

# Looping through a dictionary
dict1 = {"A": 1, "B": 2, "C": 3}
print("Looping through dict1:")
for key in dict1:
    print(key, ":", dict1[key])

# Looping through a set
set1 = {1, 2, 3, 4, 5}  
print("Looping through set1:")
for item in set1:
    print(item)